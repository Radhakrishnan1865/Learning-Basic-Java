package operators;

public class ArithmaticOperators {

	public static void main(String[] args) {
		// Arithmatic opertors  + - * / %
		
		int a=50;
		int b=20;
		
		int sum=a+b;
		System.out.println("Sum of a and b is " + sum);
		
		int sub=a-b;
		System.out.println("Subraction of a and b is " + sub);
		
		int multi=a*b;
		System.out.println("Multiplication of a and b is " +multi);
		
		int div=a/b;
		System.out.println("Division of a and b is " + div);
		
		int mdiv=a%b;
		System.out.println("Modulo division of a and b is " + mdiv);
		
		

	}

}
