package operators;

public class RelationalOperators {

	public static void main(String[] args) {
		// Relational operators return boolean value
		// < <= > >= != ==
		// use all kind of data types
		
		int a=20;
		int b=10;
		
		System.out.println(a<b);
		System.out.println(a<=b);
		System.out.println(a>b);
		System.out.println(a>=b);
		System.out.println(a!=b);
		System.out.println(a==b);

	}

}
